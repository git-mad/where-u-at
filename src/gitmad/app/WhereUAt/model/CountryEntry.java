package gitmad.app.WhereUAt.model;

public class CountryEntry {
    private String name;
    private int count;

    public CountryEntry(String name, int count) {
        this.name = name;
        this.count = count;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
